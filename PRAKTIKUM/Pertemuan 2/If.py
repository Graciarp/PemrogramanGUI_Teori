if x != True : #Jika nilai x tidak sama dengan benar
    print('print') #cetak print
else : #Jika nilai x sama dengan benar
    print('tidak print') #cetak tidak print

#penggunaan if else 2
if x != False : #Jika nilai x tidak sama dengan false
    print('print X')
elif y != False : #elif artinya else if
    print('print Y')

#penggunaan if else 3
